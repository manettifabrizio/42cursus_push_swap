#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	if (!(ptr = malloc((count * size) * sizeof(char))))
		return (NULL);
	ft_bzero(ptr, count * size);
	return (ptr);
}
